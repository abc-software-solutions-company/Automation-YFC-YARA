const reporter = require("multiple-cucumber-html-reporter");
const fs = require('fs');
const path = require('path');

// Root path (safe cho CI)
const root = process.cwd();

// Directories
const jsonDir = path.join(root, 'report', 'json');
const reportPath = path.join(root, 'report', 'html');

// Ensure directories exist
fs.mkdirSync(jsonDir, { recursive: true });
fs.mkdirSync(reportPath, { recursive: true });

// Generate report
reporter.generate({
    jsonDir: jsonDir,
    reportPath: reportPath,
    reportName: "YARA-API-Test-Report",
    pageTitle: "YARA API Automation Results",
    pageFooter: "Generated by YARA QC Team",
    customMetadata: true,
    customData: {
        title: 'Test Environment Info',
        data: [
            { label: 'Project', value: 'YARA API Automation' },
            { label: 'Environment', value: 'Pre-Prod' },
            { label: 'Test Type', value: 'YARA API Testing' },
            { label: 'Generated', value: new Date().toLocaleString() }
        ]
    },
    metadata: [
    { name: "Type", value: "API" }
    ],
    displayDuration: true,
    displayReportTime: true,
    openReportInBrowser: false,
});